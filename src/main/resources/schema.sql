create table board
(
    id           bigint generated by default as identity,
    title        varchar(255),
    content      clob,
    author       varchar(255),
    createdDate  timestamp,
    modifiedDate timestamp,
    primary key (id)
);

create table todo
(
    id        bigint generated by default as identity,
    content   varchar(255),
    completed boolean,
    toDate    date,
    primary key (id)
);
